import type {
  BodyZone,
  CapabilityDimension,
  CapabilityLevel,
  EquipmentType,
  Exercise,
  ExerciseMedia,
  ExerciseVariant,
  MovementPattern,
} from "@/lib/sport/types";

function media(exerciseId: string, altText: string): ExerciseMedia[] {
  return [
    {
      id: `${exerciseId}-media-placeholder`,
      exerciseId,
      kind: "illustration",
      placeholder: true,
      altText,
      source: "Emplacement reserve, aucun media externe importe.",
      license: "A fournir avant publication du media.",
      validationStatus: "draft_unreviewed",
    },
  ];
}

function variant(
  exerciseId: string,
  id: string,
  name: string,
  difficulty: CapabilityLevel,
  requiredEquipment: EquipmentType[],
  guidance: string,
  primaryCue: string,
  effortSecondsMin: number,
  effortSecondsMax: number,
  easierVariantId: string | null,
  harderVariantId: string | null,
): ExerciseVariant {
  return {
    id,
    exerciseId,
    name,
    difficulty,
    requiredEquipment,
    guidance,
    primaryCue,
    effortSecondsMin,
    effortSecondsMax,
    easierVariantId,
    harderVariantId,
  };
}

function exercise(args: {
  id: string;
  name: string;
  movementPattern: MovementPattern;
  capabilityDimension: CapabilityDimension;
  shortDescription: string;
  primaryCue: string;
  teachingSteps: string[];
  commonMistakes: string[];
  breathing: string;
  requiredEquipment: EquipmentType[];
  targetZones: BodyZone[];
  cautionZones: BodyZone[];
  difficulty: CapabilityLevel;
  effortSecondsMin: number;
  effortSecondsMax: number;
  variants: ExerciseVariant[];
  mediaAlt: string;
}): Exercise {
  return {
    id: args.id,
    name: args.name,
    activity: "strength",
    movementPattern: args.movementPattern,
    capabilityDimension: args.capabilityDimension,
    shortDescription: args.shortDescription,
    primaryCue: args.primaryCue,
    teachingSteps: args.teachingSteps,
    commonMistakes: args.commonMistakes,
    breathing: args.breathing,
    requiredEquipment: args.requiredEquipment,
    targetZones: args.targetZones,
    cautionZones: args.cautionZones,
    difficulty: args.difficulty,
    effortSecondsMin: args.effortSecondsMin,
    effortSecondsMax: args.effortSecondsMax,
    active: true,
    validationStatus: "draft_unreviewed",
    variants: args.variants,
    media: media(args.id, args.mediaAlt),
  };
}

