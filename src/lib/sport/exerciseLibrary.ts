export function getExerciseById(exerciseId: string): Exercise | null {
  return STRENGTH_EXERCISES.find((exerciseItem) => exerciseItem.id === exerciseId) ?? null;
}

export function getVariantById(variantId: string): ExerciseVariant | null {
  for (const exerciseItem of STRENGTH_EXERCISES) {
    const found = exerciseItem.variants.find((item) => item.id === variantId);
    if (found) {
      return found;
    }
  }

  return null;
}
